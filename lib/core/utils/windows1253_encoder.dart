  /// Κωδικοποιεί [text] σε bytes Windows-1253.
  ///
  /// ASCII (0x00–0x7F) περνά αυτούσιο. Χαρακτήρες εκτός πίνακα ρίχνουν [ArgumentError].
  static List<int> encode(String text) {
    final out = <int>[];
    for (final rune in text.runes) {
      if (rune <= 0x7F) {
        out.add(rune);
        continue;
      }
      final byte = _unicodeToByte[rune];
      if (byte == null) {
        throw ArgumentError(
          'Character U+${rune.toRadixString(16).toUpperCase()} '
          'is not representable in Windows-1253',
        );
      }
      out.add(byte);
    }
    return out;
  }
